package br.com.estudo4.operadores;

public class Bionario_aritimeticos {

	public static void main(String[] args) {
		
		int a = 11;
		int b = 2;
		
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println();
		System.out.println(a % b);
		System.out.println(Math.pow(a, b));
		

	}
}
