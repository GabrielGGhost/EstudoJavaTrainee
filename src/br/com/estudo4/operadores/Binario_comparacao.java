package br.com.estudo4.operadores;

public class Binario_comparacao {

	public static void main(String[] args) {
		
		int a = 30;
		int b = 30;
		
		System.out.println(a < b);//a é menor que b?
		System.out.println(a <= b);//a é menor ou igual à b?
		System.out.println(a > b);//a é maior que b?
		System.out.println(a >= b);//a é maior ou igual à b?
		System.out.println(a == b);//a é igual à b?
		System.out.println(a != b);//a é diferente de b?

	}

}
