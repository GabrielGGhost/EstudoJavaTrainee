package br.com.estudo4.operadores;

public class Unario_incremento_decremento {

	public static void main(String[] args) {
		
		int a = 0;
		int b = 10;
		
		//Incremento
		System.out.println(a++);
		System.out.println(a);
		
		a = 0;
		//Decremento
		System.out.println(a--);
		System.out.println(a);
	}
}
